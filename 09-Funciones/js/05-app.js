//Parametros y funciones.

//a y b son parametroc cuando se crea la funcion.
function suma( a, b ) {
  console.log( a + b );
}

//2 se asigna a A y 5 se asigna a B ya que son argumentos que le pasamos a la funcion cuando vamos a ejecutarla.
suma( 2, 5 );

function saludar(name, lastName) {
  console.log(`Hola ${name}`);
  console.log(`Hola ${lastName}`);
}

saludar('Melissa', 'Velez');